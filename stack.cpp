#include <iostream>
using namespace std;

int fun() {
    int x = 25;
    cout << "x" << x << endl;
    return x;
}

int main() {
    fun();
    cout << x << endl;
    return 0;
}







// Stack:
// Stack is a memory area where functions and local variables
// are stored temporarily during program execution.

// It follows LIFO (Last In, First Out) principle.
// The last item added to the stack is the first one removed.

// When a function finishes execution,
// its local variables are automatically removed from the stack.

// Short Definition:
// Stack is temporary memory used during function execution
// that follows the LIFO order.






